#In programming there are loops that allow you to repeat a block of code multiple times.
# There are two types of loops in Python: for loops and while loops.

# Here is an example of a for loop:
for x in range(5):
    print(x)
# This loop will print the numbers 0 through 4 and will print them out as well

# Here is an example of a while loop:
x = 0
while x < 5:
    print(x)
    x = x + 1
# This loop will print the numbers 0 through 4 and will print them out as well

